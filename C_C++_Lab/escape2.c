#include<stdio.h>
int main()
{
/* To illustrate the use of \n escape sequence */

printf("Welcome\v");
printf("to\n");
printf("DataFlair\v");
printf("tutorials!\n");

/* To illustrate the use of \n escape sequence */

printf("Welcome\tto\tDataFlair\ttutorials!");

/* To illustrate the use of \v escape sequence */

printf("Welcome\vto\vDataFlair\vtutorials!");
return 0;
}